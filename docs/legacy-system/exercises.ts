export const getExerciseById = (id: string) => exercises.find(e => e.id === id);
export const searchExercises = (q: string) => {
  const lower = q.toLowerCase();
  return exercises.filter(e => e.name.toLowerCase().includes(lower) || e.description.toLowerCase().includes(lower));
};
